<script lang="ts">
import Vue from "vue";
import { Component, Prop } from "vue-property-decorator";

@Component
export default class PageTitleComponent extends Vue {
    @Prop({ required: true }) title!: string;

    private get hasSlot(): boolean {
        return this.$slots.default !== undefined;
    }

    private get hasPrependSlot(): boolean {
        return this.$slots.prepend !== undefined;
    }
}
</script>

<template>
    <div id="pageTitle">
        <b-row no-gutters class="justify-content-between">
            <b-col cols="auto" sm>
                <h1 id="subject" class="mb-0 mt-2">
                    <slot v-if="hasPrependSlot" name="prepend" /> {{ title }}
                </h1>
            </b-col>
            <b-col v-if="hasSlot" class="mb-0 ml-2">
                <slot />
            </b-col>
        </b-row>
        <hr class="my-2" />
    </div>
</template>

